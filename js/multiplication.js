/**
 * @description returns multiplication of three elements
 * @param {int} first component of multiplication
 * @param {int} second component of multiplication
 * @param {int} third component of multiplication
 * @return {int} multiplication of two components
 */
function multiplication(first, second, third) {
    return first * second * third;
}

console.log(multiplication(2,  3, 6));

